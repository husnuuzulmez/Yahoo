class Shark {
    private String Title;
    private String Content;
    private String FlickerLink;
    private String ID;


    public void setTitle(String Title) {
        this.Title = Title;
    }
    String getTitle() {
        return Title;
    }

    public void setContent(String Content) {
        this.Content= Content;
    }
    String getContent() {
        return Content;
    }

    public void setFlickerLink(String FlickerLink) {
        this.FlickerLink = FlickerLink;
    }
    String getFlickerLink() {
        return FlickerLink;
    }

    public void setID(String ID) {
        this.ID= ID;
    }
    String getID() {
        return ID;
    }

}
